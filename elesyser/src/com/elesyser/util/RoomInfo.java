package com.elesyser.util;

public class RoomInfo {
	
	private String Name;
	private String ID;
	private String Num_seats;
	
	/**
	 * @return the Name
	 */
	public String getName() {
		return Name;
	}
	/**
	 * @param Name the Name to set
	 */
	public void setName(String Name) {
		this.Name = Name;
	}
	/**
	 * @return the ID
	 */
	public String getID() {
		return ID;
	}
	/**
	 * @param ID the ID to set
	 */
	public void setID(String ID) {
		this.ID = ID;
	}
	/**
	 * @return the Num_seats
	 */
	public String getNum() {
		return Num_seats;
	}
	/**
	 * @param Num_seats the Num_seats to set
	 */
	public void setNum(String Num_seats) {
		this.Num_seats = Num_seats;
	}

}
